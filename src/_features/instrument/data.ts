const create = (
  midiProgramNumber: number,
  nameEn: string,
  nameJa: string,
  rangeStart: number | null,
  rangeEnd: number | null,
): Instrument =>
  instrumentSchema.parse({
    midiProgramNumber,
    name: {
      en: nameEn,
      ja: nameJa,
    } satisfies Localizable<string>,
    range: asNoteRange({
      start: rangeStart,
      end: rangeEnd,
    }),
    selectionStatus: SelectionStatus.SELECTED,
  } satisfies WithKey<Instrument>);
